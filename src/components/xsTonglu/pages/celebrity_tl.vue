				<h2>一代宗师叶浅予</h2>
	 			<img src="./../../../assets/img/tl_mingren_02.jpg" class="right_img"/>
	 			<p>   叶浅予(1907.3.1-1995.5.8)，原名叶纶绮，笔名初萌、性天等。浙江桐庐人。从事画教学和以舞蹈、戏剧人物为主的国画创作。擅人物、花鸟、插图、速写等。笔墨顿挫自如，豪放爽朗，形象生动传神，风格独具，成就显著。中学时自修绘画。1926年起在上海当过柜台伙计，画过广告、教科书插图，并事时装设计、舞台美术布景。1928年任上海漫画社编辑，开始漫画创作。1929年开始创作漫画，后集成《王先生别传》和《小陈留京外史》。叶老耿直磊落、胸襟开阔，一生向往革命、向往艺术，自强不息， 受到画坛的广泛尊重和爱戴。<br />
       叶老对故乡桐庐怀有深厚感情，在他的晚年，每年都要回桐庐看看，关心家乡建设。82岁那年还将珍藏多年的从元、明、清代到现今的名人书画73幅，其中有元代王叔明的《李下隐居图》、明代周之冕的《梅竹水仙》、清代郑板桥的书法及现代齐白石、黄宾虹、徐悲鸿、张大千、潘天寿、傅抱石、吴作人等人的真迹，以及叶老自己的128幅书画，还有他收藏的宋代以后的美术论著和中外文册、画谱、印谱、碑刻等珍品787册，赠送给家乡桐庐。在赠送仪式上，桐庐县人民政府要回赠他2万元人民币，以表家乡人民心意时，老画家坚辞不受，当即挥毫在县人民政府授予的《荣誉状》上写下“此款转赠桐庐县文联”。他说：“书画是人民的财富，应该留给人民。现在有些社会风气不正，只讲金钱，不讲精神，我这样做就是要同不良风气唱唱反调，以促进树立社会的良好风气。”<br />
       先生之风，山高水长。叶浅予一生深深地热爱自己的祖国、热爱自己的民族文化传统，他以自己艺术和人生的完美结合，成为20世纪中国美术的代表画家之一。</p>
