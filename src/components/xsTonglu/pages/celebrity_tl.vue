			 	<img src="./../../../assets/img/tl_mingren_03.jpg" class="right_img"/>
			 	<p>胡家芝（1897—2010），是著名的民间剪纸艺术家，进行剪纸创作已有100多年。
       1897年，胡家芝出生于桐庐，自幼在私塾受严格的传统教育。那时候，桐庐流行一种芦茨戏，戏里鲜活的人物吸引了幼小的胡家芝，回家后她就拿起剪刀在纸上剪了起来。没想到，这一剪，她竟剪了100多年。<br />
       20岁时，胡家芝出嫁到离家30里的珠山村。溪流清澈，远山凝翠，秀丽的乡间景色为她的艺术生命注入了更深厚的生活源泉。胡家芝用剪刀剪出了无数精美绝伦的“喜花”、“灯花”和“礼花”。在珠山村生活的36年里，胡家芝成为远近闻名的剪纸能手。1952年胡家芝随家人迁居南京，从桐庐到南京，胡家芝一直没有放下剪刀。1959年，她精心创作了《祖国万岁》，讴歌新中国建设的第一个十年，一举成名。<br />
       20世纪80年代，胡家芝的剪纸艺术达到了新的高度，创作出《万象更新》、《美满人间》、《鸳鸯戏荷》等一批精品。1989年国庆前夕，胡家芝在桐庐县政府的资助下出版了《胡家芝剪纸喜花集》，并将其作为国庆礼物送回故乡，让桐庐剪纸逐渐被人所发现。2009年，桐庐县政府还拨出专款修缮了胡家芝故居，成立了胡家芝剪纸艺术馆，继承和发扬她的剪纸艺术。</p>
