					<p>无需起得太早，因为严子陵钓台距桐庐县城仅20分钟的车程。从富春江边的码头乘游轮或快艇前往严子陵钓台，沿途风光秀美，难怪有“富春江小三峡”之美誉。
那天碰巧下着细雨，虽说多了些不便，但却有别样的情趣。两岸的群峰在水色掩映下愈显婆娑之态，相比晴空万里的天气，这样的朦胧感无疑增添了游人心中更为丰富的怀想。上了严子陵钓台，反复吟读着唐代诗人孟浩然的题赋“钓矶平可坐，观其恨来晚”，无由地生发出淡然的情绪———这位东汉名士乃汉光武帝刘秀之同窗，刘秀即位后，严子陵坚辞不仕，携妻回富春江畔隐居，躬耕垂钓终老林泉。在满山的诗文碑刻和烟雨亭台之间徜徉流连，感受到的，是古代隐士的那份淡泊和潇洒，然而又想到，恐怕也只有这般清雅灵秀的所在，才能令名士放下功名的羁绊。 如果说游览严子陵钓台可以抒发人文情怀，那么，对岸的江南龙门湾则能够再次为自然风光的壮美倾心折服。泛舟水上，四周绿意葱茏，水烟缭绕，其景致不输张家界武陵源。龙门湾里的钓鱼岛曾被钓鱼行家称为“中国江河最佳钓区”，如果有足够的时间，为何不在这里享受半日的浮生闲趣呢？龙门湾的最深处是一处天然浴场，游人夏季可在此嬉水，旁边还有临流鱼味馆和烧烤场，各种河鲜的新鲜程度自然不需怀疑。 桐庐之行的最后一站可以定在大奇山国家森林公园。其自然风光在江浙一带颇负盛名，而为了凸现与众不同的特点，景区还特别开发了丰富的拓展训练项目。由于结合了自然环境本身所存在的各种险阻，大奇山的拓展训练项目相比城市中的类似活动更具价值。此外，大奇山的茶叶也相当有特色，下山时可带些回家。 </p>
