	 				<p>都市喧嚣，常思寄情山水。但周末仅两个休息日，酣畅的远足似乎缺少必要的条件。由此，江浙一带的短途旅游成为申城上班族周末休闲生活的一大选择。对于喜欢自驾游的朋友来说，如果杭州因为太过熟悉，已让你失去了起码的好奇，那么，不妨将目光稍稍放远一点———距杭州80公里的桐庐也许是另一个好去处，她远没有杭州那般熙熙攘攘，却具备了比杭州更为多元的自然景观形态。 　　
					从自驾游的角度出发，适中的距离以及便利的交通无疑也是上海游客“倾慕”桐庐的重要理由。</p>
					<p class="coloryellow" style="margin-top: 15px;">天目溪漂流—红灯笼外婆家—瑶琳仙境—桐君山七里扬帆</p>
					<p>在桐庐县城吃完午饭即可驱车沿天目溪溯流而上。在天目溪上游的毕浦码头，一段奇妙的旅程正在向我们招手。
以天目溪竹筏漂流作为桐庐之行的序曲，应当是个不错的选择———开了一上午的车，毕竟感到有些疲劳，此时闲坐于竹筏之上，任由筏工师傅将自己带往水波的中央，实在有一种悠游之趣。天目溪的水流并不湍急，却也绝非波澜不惊。常常是在你即将进入完全放松的状态之际，水势遽然转变，令心情随之上下起伏。 两岸的风景更是值得称道。《水经注》上对天目溪有这样的描述：“连山夹水，两峰交峙，反项对石，往往相捍……涧下白沙细石，状若霜雪，水木相映，泉石争辉……”从毕浦码头至红灯笼乡村家园，竹筏漂流全程约5公里。据介绍，这是天目溪风景最秀丽的一段水面。只要天气晴好，溪水通常是清澈见底，赤足享受水波的轻抚，平素放不下的种种心事恐怕早已悄然遁去。 漂流的终点便是红灯笼乡村家园，又称外婆家。拾级而上，一个个红灯笼点缀在葱绿的山林间，仅色彩带来的感观愉悦就足以使人赞叹不绝。这称得上是一处原汁原味的农家园林，有水车，有古井，有满枝青涩的柿子，有遍地熟透的南瓜。这里的民居皆青砖黛瓦，古朴但不破旧，如果想体味真正的乡村野趣，你可以选择夜宿山上的小木屋，品尝外婆家的农家土菜，观赏颇具当地风情的民俗表演。 瑶琳仙境紧挨着红灯笼乡村家园，这也许是整个桐庐不得不去的第一景点。“中国旅游胜地四十佳”和“全国诸洞之冠”等名号还不足以反映瑶琳的神奇，唯有身临其境，方能真正感佩于大自然造化之奇巧壮伟。洞内方圆近3万平方米，钟乳累累，石笋林立，奇石斗秀，清泉潺潺，而经由导游小姐的仔细讲解和“循循善诱”，你会惊异地发现，许多景观竟这般栩栩如生，越看越像。什么“狮象迎宾”啊，“熊猫戏球”啊，简直是鬼斧神工，诡谲得让人不可思议。过去我也看过很多洞，但的确没有一处可与瑶琳相提并论。 走出瑶琳，大抵已有不虚此行的感受。不过瑶琳一个半小时的游览相当耗费体力，晚上就不宜安排“体能训练”了。把车开回桐庐县城，在城外不远的桐君山七里人家享用农家晚餐是最佳的选择。被视为桐庐门户的桐君山实在是有些来历的———相传古时有位隐居的老人在此采撷草药，治病救人，他结庐桐树下，有问其姓名者，则指桐以示之。从此，人们就称其为桐君老人。李时珍的《本草纲目》中也奉他为中医药祖。 桐君山的风景并无明显特色，倒是山下七里人家的农家菜，着实做得地道。由于借着“药祖圣地”的名号，七里人家的菜色有很多都加入了“药补”的元素，比如“药膳砂锅炖土鸡”，极尽鲜美，又具有非常高的滋补价值。当地旅游界人士介绍说，七里人家已经渐渐成为桐庐旅游中“吃”这一环节的首选。 </p>
